using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConsoleApp2
{
    class Program
    {
        static void Main(string[] args)
        {
            // 리더럴 코드에서 고정된 값을 의미한다
            int number = 10;  //정수형 리터럴
            double p1 = 1.14; //실수형 리터럴
            char letter = 'A'; // 문자 리터럴
            string name = "Alive"; //문자열 리터럴;

            Console.WriteLine(number); // 출력 10
            Console.WriteLine(p1); // 출력 1.14
            Console.WriteLine(letter); // 출력 A
            Console.WriteLine(name); // 출력 Alive
        }
    }
}
